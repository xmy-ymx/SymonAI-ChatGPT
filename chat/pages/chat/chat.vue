<script setup>
import { ref } from "vue";
import { Cloud } from "laf-client-sdk";
// 将marked 引入
import { marked } from "marked";

const cloud = new Cloud({
  baseUrl: "https://ayebvv.laf.dev",
  getAccessToken: () => "",
  dbProxyUrl: "/proxy/app",
   environment: "wxmp"
});

const list = ref([]);
const question = ref("");
const parentMessageId = ref("");
const loading = ref(false);

async function send() {
  if (loading.value) return;

  list.value.push({
    text: question.value,
    avatar: "../../public/u=2742191467,3729002290&fm=253&fmt=auto&app=138&f=JPEG.webp",
  });

  setScreen();

  const message = question.value;
  question.value = "";
  loading.value = true;
  let res;
  if (message == "") {
    loading.value = false;
    list.value.push({
      text: "问题不能为空！",
      avatar: "../../public/log.png",
    });
    setScreen();
    return;
  }
  try {
    if (!parentMessageId.value) {
      res = await cloud.invoke("chat", { message });
    } else {
      res = await cloud.invoke("chat", {
        message,
        parentMessageId: parentMessageId.value,
      });
    }
  } catch (error) {
    loading.value = false;
    list.value.push({
      text: "出错了，请重试！",
      avatar: "../../public/log.png",
    });
    setScreen();
    return;
  }

  parentMessageId.value = res.id;

  res.text = res.text.replace(/\n/g, "<br>");
  res.text = marked.parse(res.text);

  list.value.push({
    text: res.text,
    avatar: "../../public/log.png",
  });

  loading.value = false;
  setScreen();
}

function setScreen() {
  setTimeout(() => {
    // window.scrollTo(0, document.body.scrollHeight);
  }, 0);
}
